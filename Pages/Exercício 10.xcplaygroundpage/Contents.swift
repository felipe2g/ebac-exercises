// Escreva a diferença entre struct e class?

// A principal diferença entre os dois é que Classes são do tipo de referência, Structs são objetos do tipo value.
// Classes podem definir um “destrutor” deinit() que são funções chamadas antes da instância ser desalocada.
// Classes podem ter uma ou mais referências para uma única instância.

// Diga qual a relação entre classe e objeto.

// Em uma analogia, podemos pensar que a classe é uma planta de uma casa, um planejamento a ser seguido, nesta está definido todas suas características básicas de como ela deverá ser composta. A classe é algo abstrato que não tem um tempo de vida definido.
// Já o objeto é algo é algo concreto, algo físico, em uma analogia o objeto é a casa construída à partir da planta acima, esse objeto possui valores e estados definidos também à partir da classe acima e tem um tempo de vida transitório.

// Qual a diferença entre uma classe e um objeto?
// A classe é algo abstrato, que por si só não possui valores instrínsecos. O objeto é algo palpável, que foi criado a partir da classe em questão.
